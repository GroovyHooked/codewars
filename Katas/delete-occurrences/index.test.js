const deleteNth = require("./index.js");
test("test", () => {
  expect(deleteNth([20, 37, 20, 21], 1)).toStrictEqual([20, 37, 21]);
  expect(deleteNth([1, 1, 3, 3, 7, 2, 2, 2, 2], 3)).toStrictEqual([
    1, 1, 3, 3, 7, 2, 2, 2,
  ]);
  expect(deleteNth([1, 2, 3, 1, 1, 2, 1, 2, 3, 3, 2, 4, 5, 3, 1], 3)).toStrictEqual(
    [ 1, 2, 3, 1, 1, 2, 2, 3, 3, 4, 5 ]
  );
  expect(
    deleteNth(
      [
        24, 45, 40, 32, 32, 9, 45, 44, 44, 32, 24, 32, 9, 45, 24, 40, 45, 24, 9,
        9, 32, 9, 24, 4, 9, 45, 40, 45, 24, 32, 32, 40, 45, 32, 9, 45, 32, 4,
        44, 32, 40, 9, 40, 45, 9, 40,
      ],
      4
    )
  ).toStrictEqual([
    24, 45, 40, 32, 32, 9, 45, 44, 44, 32, 24, 32, 9, 45, 24, 40, 45, 24, 9, 9,
    4, 40, 40, 4, 44,
  ]);
  expect(
    deleteNth(
      [
        46, 20, 18, 46, 46, 36, 9, 9, 20, 18, 23, 21, 21, 21, 20, 50, 36, 36,
        21, 20, 36, 23, 18, 46, 36, 9, 21, 9, 36, 20, 36, 9, 48, 18, 18, 20, 23,
        18, 48, 36, 36, 48, 18, 21, 48, 23, 20,
      ],
      5
    )
  ).toStrictEqual([
    46, 20, 18, 46, 46, 36, 9, 9, 20, 18, 23, 21, 21, 21, 20, 50, 36, 36, 21,
    20, 36, 23, 18, 46, 36, 9, 21, 9, 20, 9, 48, 18, 18, 23, 48, 48, 48, 23,
  ]);
  expect(deleteNth([33, 4, 50, 50, 50, 33, 4, 33, 4, 33, 4], 7)).toStrictEqual([
    33, 4, 50, 50, 50, 33, 4, 33, 4, 33, 4,
  ]);
  expect(deleteNth([15, 15, 35, 35, 15, 35, 15, 31, 15, 15, 15], 2)).toStrictEqual([
    15, 15, 35, 35, 31,
  ]);
  expect(
    deleteNth([8, 24, 24, 23, 24, 8, 23, 24, 8, 23, 23, 24, 24, 24, 8, 23], 3)
  ).toStrictEqual([8, 24, 24, 23, 24, 8, 23, 8, 23]);
  expect(
    deleteNth(
      [32, 30, 32, 32, 32, 32, 30, 3, 32, 46, 39, 32, 39, 30, 32, 46, 39, 3],
      9
    )
  ).toStrictEqual([
    32, 30, 32, 32, 32, 32, 30, 3, 32, 46, 39, 32, 39, 30, 32, 46, 39, 3,
  ]);
  expect(
    deleteNth(
      [
        29, 27, 28, 29, 48, 28, 17, 27, 17, 48, 42, 17, 17, 17, 27, 42, 19, 17,
        17, 3, 28, 19, 42, 19, 3, 19, 42, 48, 12, 3, 42, 48, 12, 17, 42, 29, 29,
        48, 48, 3, 29, 17, 28, 42, 29, 28, 48, 28, 28, 48, 42, 19, 3,
      ],
      8
    )
  ).toStrictEqual([
    29, 27, 28, 29, 48, 28, 17, 27, 17, 48, 42, 17, 17, 17, 27, 42, 19, 17, 17,
    3, 28, 19, 42, 19, 3, 19, 42, 48, 12, 3, 42, 48, 12, 17, 42, 29, 29, 48, 48,
    3, 29, 28, 42, 29, 28, 48, 28, 28, 48, 42, 19, 3,
  ]);
});
